namespace RayCarrot.RCP.Metro;

/// <summary>
/// The available shell thumbnail and icon sizes
/// </summary>
public enum ShellThumbnailSize
{
    /// <summary>
    /// The default size
    /// </summary>
    Default,

    /// <summary>
    /// The small size, 16px for icons or 32px for thumbnails
    /// </summary>
    Small,

    /// <summary>
    /// The medium size, 32px for icons or 96px for thumbnails
    /// </summary>
    Medium,

    /// <summary>
    /// The large size, 48px for icons or 256px for thumbnails
    /// </summary>
    Large,

    /// <summary>
    /// The extra large size, 256px for icons or 1024px for thumbnails
    /// </summary>
    ExtraLarge,
}